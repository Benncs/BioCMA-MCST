  KernelInline::DispatchOptions
  read_options() noexcept
  {
    const auto p_p_t_contribs = Common::read_env_or(
        "BIOMC_PARTICLES_PER_TEAM_CONTRIBS",
        AutoGenerated::Kernels::particle_per_team_contributions);

    const auto p_p_t_cycle
        = Common::read_env_or("BIOMC_PARTICLES_PER_TEAM_CYCLE",
                              AutoGenerated::Kernels::particle_per_team_cycle);

    const auto p_p_t_move
        = Common::read_env_or("BIOMC_PARTICLES_PER_TEAM_MOVE", 1024UL);

    return { .f_multi_compartment = false,
             .m_p_p_team_model = p_p_t_cycle,
             .m_p_p_team_contribs = p_p_t_contribs,

             .m_p_p_team_move = p_p_t_move,
             .m_p_p_team_leave = 0 };
  }

