  void
  SimulationUnit::setMtrModel(MassTransfer::Type::MtrTypeVariant&& variant)
  {
    if (is_two_phase_flow)
    {
      this->mt_model = Simulation::MassTransfer::MassTransferModel(
          std::move(variant), liquid_scalar, gas_scalar);
    }
  }

